--What are the details of the top 10 teams which Maryland has won against keeping a shutout?

SELECT TOP 10  o.oppName AS 'Opponent Name', 
COUNT(m.mchId) AS 'Number of Shutouts'
FROM [PPP.Match] m, [PPP.Opponent] o, [PPP.Play] p
WHERE p.oppId=o.oppId
AND m.mchScrOpp=0
AND m.mchId=p.mchId
AND m.mchScrUmd>m.mchScrOpp
GROUP BY p.oppId, o.oppName
ORDER BY 'Number of Shutouts' DESC

----------------------------------------------------------------------------------------------

--What are the details top 10 away locations where Maryland has won the most number of times?

SELECT TOP 10  l.locState AS 'Location State', 
l.locCty AS 'Location City', 
COUNT (CASE WHEN m.mchScrUmd > m.mchScrOpp THEN 1 END) AS 'Match Won'
FROM [PPP.Match] m, [PPP.Location] l
WHERE l.locId=m.locId
AND mchAt='Away'
GROUP BY l.locCty, l.locId,locState
ORDER BY 'Match Won' DESC

----------------------------------------------------------------------------------------------

--What are the details of the top 10 teams that Maryland has lost against the maximum number of times?

SELECT TOP 10  o.oppName AS 'Opponent Name', 
COUNT (CASE WHEN m.mchScrUmd < m.mchScrOpp THEN 1 END) 
AS 'Number of Times Match Lost'
FROM [PPP.Match] m, [PPP.Opponent] o, [PPP.Play] p
WHERE p.oppId=o.oppId
AND m.mchId=p.mchId
GROUP BY p.oppId, o.oppName
ORDER BY COUNT (CASE WHEN m.mchScrUmd < m.mchScrOpp THEN 1 END) DESC

----------------------------------------------------------------------------------------------

--What are the details of the teams against which Maryland has won the maximum number of times?

SELECT TOP 10 o.oppName AS 'Opponent Name',
COUNT (CASE WHEN m.mchScrUmd > m.mchScrOpp THEN 1 END) AS
'Maryland wins'
FROM [PPP.Match] m, [PPP.Opponent] o, [PPP.Play] p
WHERE p.oppId=o.oppId
AND m.mchId=p.mchId
GROUP BY p.oppId,o.oppName
ORDER BY 'Maryland wins' DESC

----------------------------------------------------------------------------------------------

--What are the details of the teams against which Maryland has Lost by the highest margin?

SELECT TOP 10 o.oppName AS 'Opponent Name',
(m.mchScrUmd-m.mchScrOpp) AS 'Score Margin'
FROM [PPP.Match] m, [PPP.Opponent] o, [PPP.Play] p
WHERE p.oppId=o.oppId
AND m.mchId=p.mchId
AND m.mchScrUmd<m.mchScrOpp
ORDER BY 'Score Margin' ASC





